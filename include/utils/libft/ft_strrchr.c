#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	len;

	len = ft_strlen(s);
	while (s[len] != (char)c)
	{
		if (len == 0)
			return (NULL);
		len--;
	}
	return ((char *)s + len);
}
